// ─── Seed function ────────────────────────────────────────────────────────────

export async function seedDatabase(db: DB): Promise<void> {
  // Check if already seeded
  const existing = await db.select({ id: surahs.id }).from(surahs).limit(1);
  if (existing.length > 0) return;

  // Insert surahs
  await db.insert(surahs).values(
    SURAHS.map(([number, nameArabic, namePashto, nameDari, nameTransliteration, totalVerses, revelationType]) => ({
      id: number as number,
      number: number as number,
      nameArabic: nameArabic as string,
      namePashto: namePashto as string,
      nameDari: nameDari as string,
      nameTransliteration: nameTransliteration as string,
      totalVerses: totalVerses as number,
      revelationType: revelationType as "meccan" | "medinan",
    }))
  );

  // Insert juz
  await db.insert(juz).values(
    JUZ.map(([number, startSurah, startVerse, endSurah, endVerse, nameArabic, namePashto, nameDari]) => ({
      number: number as number,
      startSurah: startSurah as number,
      startVerse: startVerse as number,
      endSurah: endSurah as number,
      endVerse: endVerse as number,
      nameArabic: nameArabic as string,
      namePashto: namePashto as string,
      nameDari: nameDari as string,
    }))
  );
}
